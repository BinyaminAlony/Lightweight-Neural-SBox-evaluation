def multibit_walsh_transform(f):
    """
    Compute the Walsh transform of a boolean function f.
    Parameters:
        f : list of int
            A boolean function represented as a list of integers (0s and 1s).
    Returns:
        list of int
            The Walsh transform of the function f.
    """
    n = len(f)
    W = [[0] * (n-1)] * n
    for u in range(n):
        for v in range(1,n):
            for x in range(n):
                W[u][v-1] += (-1)**((v&f[x]) ^ (u & x).bit_count() % 2)
    return W

def max_multibit_walsh_transform(f):
    """
    Compute the Walsh transform of a boolean function f.
    Parameters:
        f : list of int
            A boolean function represented as a list of integers (0s and 1s).
    Returns:
        list of int
            The Walsh transform of the function f.
    """
    n = len(f)
    max_W = 0
    for u in range(n):
        for v in range(1,n):
            wtemp = 0
            for x in range(n):
                wtemp += (-1)**(((v & f[x]).bit_count() % 2) ^ ((u & x).bit_count() % 2))
            max_W = max(max_W, abs(wtemp))
    return max_W

